/** id로 별자리 조회 (없으면 null) */
export function getConstellation(id) {
  return CONSTELLATIONS.find((c) => c.id === id) || null;
}

/** 계절별 별자리 목록 */
export function constellationsBySeason(season) {
  return CONSTELLATIONS.filter((c) => c.season === season);
}
